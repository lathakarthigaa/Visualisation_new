package householdApplianceGUI;

public class AppliancesPower {
	private int fridgePower;
	private int lightPower;
	private int aggregatedPower;
	public int getFridgePower() {
		return fridgePower;
	}
	public void setFridgePower(int fridgePower) {
		this.fridgePower = fridgePower;
	}
	public int getLightPower() {
		return lightPower;
	}
	public void setLightPower(int lightPower) {
		this.lightPower = lightPower;
	}
	public int getAggregatedPower() {
		return aggregatedPower;
	}
	public void setAggregatedPower(int aggregatedPower) {
		this.aggregatedPower = aggregatedPower;
	}
}
